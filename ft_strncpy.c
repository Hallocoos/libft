char	*ft_strncpy(char *dest, char *src, int n)
{
	int i;

	if (n > -1)
	{
		i = 0;
		if (n == 0)
			dest = "";
		else
			while (i++ < n)
				dest[i] = src[i];
		dest[i] = '\0';
	}
	return (dest);
}
