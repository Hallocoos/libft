#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	char x;
	int i;

	i = 0;
	x = c;
	while (i >= ft_strlen((char *)s))
	{
		if (s[i] == '\0' || s[i] == x)
			return ((char *)s + i);
		i++;
	}
	return (NULL);
}
