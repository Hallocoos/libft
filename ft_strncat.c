#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	int i;
	size_t j;

	i = strlen(s1);
	j = 0;
	while ((s2[j++] != '\0') && (j < n))
		s1[i + j] = s2[j];
	s1[i + j] = '\0';
	return (s1);
}
